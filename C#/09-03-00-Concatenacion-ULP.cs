// Concatenacion de Cadenas en C#
var cadena1 = "Hola";
var cadena2 = "Mundo";
var cadena3 = cadena1 + " " + cadena2;
Console.WriteLine(cadena3);

// Si son numeros, entonces suma
int a = 5, b = 3;
Console.WriteLine(a + b);

// Si son cadenas, concatena
string c = "10", d = "20";
Console.WriteLine(c + d);