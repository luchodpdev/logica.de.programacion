// Operadores de Asignacion
// Operador = para asignar un valor
var miNumero = 10;
Console.WriteLine($"Valor miNumero: {miNumero}");
miNumero = 20;
Console.WriteLine($"Valor miNumero: {miNumero}");
// Operador +=
miNumero += 5;
// miNumero = miNumero + 5
Console.WriteLine($"Valor miNumero (+=5): {miNumero}");
// Operador -=
miNumero -= 3;
Console.WriteLine($"Valor miNumero (-=3): {miNumero}");
// Operador *=
miNumero *= 2;
Console.WriteLine($"Valor miNumero (*=2): {miNumero}");
// Operador /=
miNumero /= 7;
Console.WriteLine($"Valor miNumero (/=7): {miNumero}");
// Operador %= (modulo o residuo division)
miNumero %= 2;
Console.WriteLine($"Valor miNumero (%=2): {miNumero}");

