// Operadores Aritmeticos
// Suma +
var a = 3 + 4;
Console.WriteLine($"Suma: {a}");
// Resta -
var b = 6 - 2;
Console.WriteLine($"Resta: {b}");
// Multiplicacion *
var c = a * 2;
Console.WriteLine($"Multiplicacion: {c}");
// Division
var d = b / 2.5;
Console.WriteLine($"Division: {d}");
// Modulo (residuo division)
var f = 9 % 2;
Console.WriteLine($"Residuo Division: {f}");
// Potencia
var e = Math.Pow(a, 2);
Console.WriteLine($"Potencia: {e}");