// Operadores Relacionales  ( Comparacion )
var a = 5;
var b = 6;
Console.WriteLine($"Valor a: {a}");
Console.WriteLine($"Valor b: {b}");
// Operador igualdad
var c = a == b;
Console.WriteLine($"a igual que b? {c}");
// Operador distinto
c = a != b;
Console.WriteLine($"a distinto que b? {c}");
// Operador Mayor que
c = a > b;
Console.WriteLine($"a mayor que b? {c}");
// Operador Mayor o igual que
c = a >= b;
Console.WriteLine($"a mayor o igual que b?  {c}");
// Operador Menor que
c = a < b;
Console.WriteLine($"a menor que b? {c}");
// Operador Menor o igual que
c = a <= b;
Console.WriteLine($"a menor o igual que b? {c}");