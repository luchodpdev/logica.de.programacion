// Operadores Incremento
var a = 0;
// Operador post-incremento
Console.WriteLine($"Antes de incrementar: {a}");
a++;
Console.WriteLine($"Despues de incrementar (a++): {a}");
// Operador pre-incremento
a = 5;
Console.WriteLine($"Antes de incrementar: {a}");
++a;
Console.WriteLine($"Despues de incrementar (++a): {a}");

// Operadores Decremento
// Post-decremento
a = 10;
Console.WriteLine($"Antes de decrementar: {a}");
a--;
Console.WriteLine($"Despues de decrementar (a--): {a}");
// Pre-decremento
a = 20;
Console.WriteLine($"Antes de decrementar: {a}");
--a;
Console.WriteLine($"Despues de decrementar (--a): {a}");

